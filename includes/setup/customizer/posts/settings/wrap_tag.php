use GrottoPress\Jentil\Setup;

/**
 * Content wap tag setting
 *
 * Add setting and control for our content wrap tag
 * setting in the customizer.
 *
 * @link			https://jentil.grotttopress.com
 * @package			jentil
 * @subpackage 	    jentil/includes
 * @since			Jentil 0.1.0
 */
final class Wrap_Tag extends Setting {
    /**
	 * Constructor
	 *
	 * @since       Jentil 0.1.0
	 * @access      public
	 */
	public function __construct( $content ) {
        parent::__construct( $content );
        
        $this->mod = $this->mod( 'wrap_tag' );
        
        $this->name = $this->mod->get( 'name' );
        
        $this->args['default'] = $this->mod->get( 'default' );
        $this->args['sanitize_callback'] = 'sanitize_key';

        $this->control['label'] = esc_html__( 'Wrapper tag', 'jentil' );
        $this->control['type'] = 'text';
	}
}